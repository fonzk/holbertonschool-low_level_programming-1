#include "3-calc.h"

/**
 * op_add - function that adds
 * @a: first numner
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtracts
 * @a: first number
 * @b: second numner
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies
 * @a: first number
 * @b: second number
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides
 * @a: first number
 * @b: second number
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that gets the remainder
 * @a: first number
 * @b: second number
 * Return: remainder of a divided by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
